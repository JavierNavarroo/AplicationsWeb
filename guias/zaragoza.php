									del edificio que amenazaba ruina, gracias a las donaciones propiciadas por la mencionada bula papal.
									<br>
									La historia documentada del templo se remonta al siglo IX, cuando según la Historia del traslado de San Vicente de Aimoino, se atestigua 
									la existencia de una iglesia mozárabe en Saraqusta dedicada a Santa María, en el mismo lugar en el que actualmente se encuentra la basílica
									barroca. En torno a este templo se articulaba una de las comunidades de cristianos de la ciudad.
									Arquitectónicamente, la basílica se articula en tres naves, de igual altura, cubiertas con bóvedas de cañón, 
									en las que se intercalan cúpulas y bóvedas de plato, que descansan sobre robustos pilares. El exterior es de ladrillo,
									siguiendo la tradición de construcción en ladrillo aragonesa, y el interior revocado en estuco. La nave central se halla dividida 
									por la presencia del altar mayor bajo la cúpula central, con el gran retablo mayor de la Asunción, perteneciente a la iglesia anterior, 
									realizado por el escultor Damián Forment en el siglo XVI. Bajo las otras dos cúpulas elípticas de la nave central, se dispuso la Santa
									Capilla de la Virgen del Pilar, y el coro y órgano, que también procedían de la iglesia gótica predecesora. Actualmente se encuentran 
									desplazados al tramo de los pies del templo, para dotar de mayor espacio a los fieles que ocupan la nave desde el altar mayor.
									<br>
									La basílica del Pilar, junto con los santuarios de Torreciudad, Montserrat, Meritxell y Lourdes conforman la Ruta mariana, 
									itinerario guiado por la espiritualidad y devoción mariana, poseedor de una gran riqueza patrimonial, gastronómica y natural. 
									Es además desde 2007, uno de los 12 Tesoros de España.
