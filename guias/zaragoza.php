							<h2 align="center"><strong>Palacio de la Aljafería</strong></h2>
							<p style="text-align:justify">
								<em>
									El palacio de la Aljafería es un palacio fortificado construido en Zaragoza en la segunda mitad del siglo XI por iniciativa de 
									al-Muqtadir como residencia de los reyes hudíes de Saraqusta. Este palacio de recreo refleja el esplendor alcanzado por el reino taifa en el periodo de su máximo apogeo político y cultural.
									Su importancia radica en que es el único testimonio conservado de un gran edificio de la arquitectura islámica hispana de la época 
									de las taifas. 
									<br>
									Los «restos mudéjares del palacio de la Aljafería» fueron declarados 
									individualmente Patrimonio de la Humanidad por la Unesco en 1986 como parte del conjunto «Arquitectura mudéjar de Aragón».
									Las soluciones adoptadas en la ornamentación del palacio de la Aljafería, como la utilización de arcos mixtilíneos y de los salmeres en «S», 
									la extensión del ataurique calado en grandes superficies o la esquematización y abstracción progresiva de las yeserías de carácter vegetal, 
									influyeron decisivamente en el arte almorávide y almohade tanto del Magreb como de la península ibérica. Asimismo, la transición de la 
									decoración hacia motivos más geométricos está en la base del arte nazarí.
									Tras la reconquista de Zaragoza en 1118 por Alfonso I el Batallador pasó a ser residencia de los reyes cristianos de Aragón, con lo que la
									Aljafería se convirtió en el principal foco difusor del mudéjar aragonés. Fue utilizada como residencia regia por Pedro IV el Ceremonioso 
									(1319-1387) y posteriormente, en la planta principal, se llevó a cabo la reforma que convirtió estas estancias en palacio de los Reyes 
									Católicos en 1492. En 1593 experimentó otra reforma que la convertiría en fortaleza militar, primero según diseños renacentistas 
									(que hoy se pueden observar en su entorno, foso y jardines) y más tarde como acuartelamiento de regimientos militares. 
									Sufrió reformas continuas y grandes desperfectos, sobre todo con los Sitios de Zaragoza de la Guerra de la Independencia hasta que 
									finalmente fue restaurada en la segunda mitad del siglo XX y actualmente acoge las Cortes de Aragón.
									En su origen la construcción se hizo extramuros de la muralla romana, en el llano de la saría o lugar donde los musulmanes 
									desarrollaban los alardes militares conocido como La Almozara. Con la expansión urbana a través de los años, el edificio ha quedado 
									dentro de la ciudad. Se ha podido respetar a su alrededor un pequeño entorno ajardinado.
								</em>
