							<h2 align="center"><strong>Puente de Piedra</strong></h2>
							<p style="text-align:justify">
								<em>
									Aunque el puente conservado data del siglo XV, ha quedado referencia de que en época romana hubo un puente que fue destruido hacia el 
									año 827-828 y luego reconstruido por Abderramán II en 839. 
									<br>
									El que se ha conservado de 1440 tiene unos 225 metros de longitud sobre 
									siete arcos (uno de los cuales ha quedado enterrado por el Paseo Echegaray). 
									<br>
									Con luces que oscilan entre los 14 a los 32 metros, 
									los arcos se apoyan en pilares de desigual ancho, con tajamares para desviar el agua.
									<br>
									Una riada el año 1643 destruyó dos de las arcadas centrales del puente, circunstancia que se documenta en la Vista de Zaragoza 
									pintada en 1647 por Martínez del Mazo. Era el tramo donde se irguieron dos torrecillas de origen medieval. El puente fue reparado 
									en 1659, incluyendo las dos torres, por Felipe de Busignac, maestro de obras de Zaragoza, que modificó también los tajamares y 
									espolones del puente, ampliándolos. 
									<br>
									La piedra utilizada para construir el puente fue traída de la cantera que tenía el Papa en 
									el monte de Burrén, entre los pueblos de Fréscano y Bisimbre. En su origen el puente estuvo flanqueado por torres que se demolieron 
									en 1906.
									<br>
									Fue remodelado en el siglo XX en varias ocasiones; en 1991 se añadieron cuatro leones de bronce, obra del escultor Francisco Rallo 
									Lahoz. 
									<br>
									Se instalaron luminarias en cuyos postes se colocaron medallones con el perfil de los mismos leones.
									Este conjunto monumental, que a nivel nacional supuso un gran avance ya que desde mediados del siglo XV el puente garantizó las 
									comunicaciones del cuadrante noreste peninsular, se ha convertido en uno de los elementos distintivos de la ciudad de Zaragoza.
									<br>
									Para prevenir el riesgo de inundaciones de la ciudad en las posibles avenidas del Ebro, a finales del siglo XVIII el arquitecto
									Agustín Sanz inició la construcción de un muro de contención en la margen izquierda que iría desde el Puente de Piedra hasta el 
									Convento de San Lázaro. Se construyó en seis meses concluyéndose las obras el 24 de diciembre de 1789.
									
