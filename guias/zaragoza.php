									La catedral-basílica de Nuestra Señora del Pilar o Santo Templo Metropolitano de Nuestra Señora del Pilar de Zaragoza es un importante
									templo barroco de España.
									Según la tradición, se trata del primer templo mariano de la Cristiandad, puesto que en él se conserva y venera el pilar que, 
									según la tradición, fue puesto por la Virgen María quien, viviendo aún en Jerusalén, se habría aparecido 
